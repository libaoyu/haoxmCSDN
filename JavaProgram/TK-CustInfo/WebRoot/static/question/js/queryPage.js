
$(function () {
    // 设置页面字体大小
    $("input[name='fontS']").click(function () {
        var value = $(this).val();
        if (value === '大') {
            $('body').css('font-size','18px');
            // 题目标题
            $('.ti_b1').css('font-size','20px');
            $('.contentFontSize').css('font-size','14px');
            // 选项字体
            $('.table-bordered').css('font-size','16px');
        } else if (value === '中') {
            $('body').css('font-size','16px');
            $('.ti_b1').css('font-size','18px');
            $('.contentFontSize').css('font-size','14px')
            // 选项字体
            $('.table-bordered').css('font-size','14px');
        } else if (value === '小') {
            $('body').css('font-size','14px');
             // 选项字体
            $('.table-bordered').css('font-size','12px');
            $('.ti_b1').css('font-size','15px');
            
        }
    });
});
