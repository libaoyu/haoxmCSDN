package com.fh.entity.enabler;

/**
 * 
* 类名称：NotifyCallEvent
* 类描述：华为小号事件通知接口实体类
* @author shanghaizhao
* @date 2018-01-03 
 */
public class NotifyCallEvent {
	
	private String callEventId;// 呼叫事件ID
	private String callIdentifier;//呼叫唯一标识
	private String called;//真实被叫号码
	private String calling;//真实主叫号码
	private String event;//呼叫状态事件
	private ExtInfo extInfo;//扩展呼叫事件信息对象
	private String isRecord;//是否录音
	private String notificationMode;//通知模式
	private String timeStamp;//呼叫事件发生的时间戳
	private String virtualNumber;//虚拟号码
	private String extInfoId;// 扩展呼叫事件ID
	
	
	public String getExtInfoId() {
		return extInfoId;
	}
	public void setExtInfoId(String extInfoId) {
		this.extInfoId = extInfoId;
	}
	public String getCallEventId() {
		return callEventId;
	}
	public void setCallEventId(String callEventId) {
		this.callEventId = callEventId;
	}
	public String getCallIdentifier() {
		return callIdentifier;
	}
	public void setCallIdentifier(String callIdentifier) {
		this.callIdentifier = callIdentifier;
	}
	public String getCalled() {
		return called;
	}
	public void setCalled(String called) {
		this.called = called;
	}
	public String getCalling() {
		return calling;
	}
	public void setCalling(String calling) {
		this.calling = calling;
	}
	public String getEvent() {
		return event;
	}
	public void setEvent(String event) {
		this.event = event;
	}
	public ExtInfo getExtInfo() {
		return extInfo;
	}
	public void setExtInfo(ExtInfo extInfo) {
		this.extInfo = extInfo;
	}
	public String getIsRecord() {
		return isRecord;
	}
	public void setIsRecord(String isRecord) {
		this.isRecord = isRecord;
	}
	public String getNotificationMode() {
		return notificationMode;
	}
	public void setNotificationMode(String notificationMode) {
		this.notificationMode = notificationMode;
	}
	public String getTimeStamp() {
		return timeStamp;
	}
	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}
	public String getVirtualNumber() {
		return virtualNumber;
	}
	public void setVirtualNumber(String virtualNumber) {
		this.virtualNumber = virtualNumber;
	}

	
}
