package com.fh.entity.enabler;

/**
 * 
* 类名称：Extparas
* 类描述：华为小号事件通知接口实体类扩展呼叫事件信息扩展信息类
* @author shanghaizhao
* @date 2018-01-03 
 */
public class Extparas {
	
	private String extParasId;//扩展信息类ID
	private String releaseReason;//呼叫结束原因(只有Release事件包含)
	private String uniqueId;//标识消息的唯一Id
	private String bindId;//绑定关系
	private String startTime;//呼叫开始的时间戳
	private String duration;//通话时长
	
	
	public String getExtParasId() {
		return extParasId;
	}
	public void setExtParasId(String extParasId) {
		this.extParasId = extParasId;
	}
	public String getReleaseReason() {
		return releaseReason;
	}
	public void setReleaseReason(String releaseReason) {
		this.releaseReason = releaseReason;
	}
	public String getUniqueId() {
		return uniqueId;
	}
	public void setUniqueId(String uniqueId) {
		this.uniqueId = uniqueId;
	}
	public String getBindId() {
		return bindId;
	}
	public void setBindId(String bindId) {
		this.bindId = bindId;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getDuration() {
		return duration;
	}
	public void setDuration(String duration) {
		this.duration = duration;
	}
	

}
